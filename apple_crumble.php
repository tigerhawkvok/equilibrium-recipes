<ul>
<li>4 firm medium apples</li>
<li>1 cup sugar</li>
<li>1 cup all-purpose flour</li>
<li>1 butter stick</li>
<li>1 tablespoon ground cinnamon</li>
</ul>
<p class="process">Wash and core apples, cut into dices. Place in a baking dish.
  Sprinkle the cinnamon over the apple. Mix the sugar and flour in a a bowl and add 
  the melt butter. Rub in with fingertips until the topping comes together in moist 
  clumps. Sprinkle the topping over the apple. Bake the crumble until apples are tender 
  and the topping appears brown and crisp, about 45 minutes @400.</p>
