<ul>
<li>6 firm medium apples</li>
<li>1 1/2 cups sugar</li>
<li>1 1/2 cups unsweetened cranberry juice</li>
<li>1/4 cup lemon juice</li>
<li>2 sticks cinnamon</li>
<li>3 cloves</li>
</ul>
<p class="process">Wash and core apples, cut into thick slices. Place
  in flat pan. Combine other ingredients, boil 5 mins. Save 1/4 cup
  of mixture, pour rest over apples, bake @ 350 for 45 mins, basting occasionally.</p>
