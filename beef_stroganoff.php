<ul>
<li>2 pounds beef (Veggie: 2pkg tofu + 2 tsp Bragg's)</li>
<li>1.5 tsp dill</li>
<li>1/2 teaspoon salt</li>
<li>1/2 teaspoon pepper</li>
<li>4 oz. butter</li>
<li>6 green onions (chopped)</li>
<li>4 tablespoons flour or cornstarch</li>
<li>2 cups chicken broth</li>
<li>~ 1/2 Tbsp mustard</li>
<li>1.5 cups mushrooms (sliced)</li>
<li>1/2 cup sour cream (full, or slightly more)</li>
<li>~ 1/3 cup white wine</li>
<li>2 lbs egg noodles</li>
</ul>
<ol>
<li>Cut beef into strips and season with salt and pepper</li>
<li>In skillet, melt the butter and brown the beef.  Push them off to one side.  Add onions and mushrooms and cook for 3-5 minutes, then push to the side with beef.</li>
<li>Stir in flour into the juices on empty side of pan.  Pour in broth and bring to a boil, stirring constantly.  Lower the heat and stir in mustard.  Cover and simmer for ~ 1/2 hour.</li>
<li>Five minutes before serving, stir in sour cream and white wine.  Heat briefly then serve over noodles.  Serves ~ 4.</li>
</ol>

