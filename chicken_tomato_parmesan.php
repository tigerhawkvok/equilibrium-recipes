<ul id='ingredients'>
  <!-- Ingredients in unordered list -->
  <li>2 eggs, beaten</li>
  <li>1 cup grated Parmesan cheese</li>
  <li>5 ounces seasoned bread crumbs (with plain bread crumbs: add oregano, onion powder, garlic powder, basil) | Substitute: 50% flour, 50% corn starch</li>
  <li>2 skinless, boneless chicken breast halves</li>
  <li>1 tablespoon olive oil</li>
  <li> &gt;= 15 ounces pasta sauce</li>
  <li>6 slices Monterey Jack cheese</li>

</ul>

<ol id='process'>
  <!-- Steps as ordered list elements -->
  <li>Cut chicken breasts into smallish chunks, about the size of a C-cell battery.</li>
  <li>Pour tomato sauce into a saucepan.  Cook over medium heat, adding onions, basil, oregano, and garlic to taste.</li>
  <li>Preheat oven to 375 degrees F (190 degrees C).</li>
  <li>Pour beaten eggs into a shallow dish or bowl. In another shallow dish or bowl, mix together the grated Parmesan cheese and bread crumbs. Dip chicken breasts into beaten egg, then into bread crumb mixture to coat.</li>
  <li>In a large skillet, heat oil over medium high heat. Add coated chicken and saute for about 8 to 10 minutes each side, or until chicken is cooked through and juices run clear.  The breading should turn a shade of golden brown. If using an air fryer, set to 350F for ~9 minutes.</li>
  <li>Add chicken into a lightly greased pie baking dish -- try to spread it out into a single layer deep.  Pour tomato sauce in, then place chunks of Monterey Jack cheese evenly over the dish.</li>
  <li> Bake in the preheated oven for 20 minutes or until cheese is completely melted.  Serve over noodles.</li>


</ol>

