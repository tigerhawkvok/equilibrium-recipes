<ul id='ingredients'>
  <li>3 eggs</li>
  <li>3/4 cup milk</li>
  <li>2 shots spiced rum</li>
  <li>2 cup chocolate chips (melted)</li>
  <li>3/4 cup unsweetened cocoa powder</li>
  <li>1 cup boiling water</li>
  <li>1.75 cups flour</li>
  <li>2 cups white sugar</li>
  <li>1.5 tsp baking powder</li>
  <li>1.5 tsp baking soda</li>
  <li>1 tsp salt</li>
  <li>2 stick butter (melted)</li>
  <li>1 tbsp vanilla</li>
</ul><br />
<ol id='process'>
  <li>Preheat oven to 350 F</li>
  <li>Mix sugar, flour, cocoa, baking powder, baking soda, and salt.</li>
  <li>Mix in eggs, 1/4 cup milk, and melted butter</li>
  <li>Melt chocolate chips, the rest of the milk, vanilla, and rum in a double boiler. Once melted, add the boiling water and mix thoroughly.</li> 
  <li>Mix in chocolate chips thoroughly, then stir in boiling water.</li>
  <li>Pour into 1 9x13 pan, and bake for 60 minutes</li>
  <li>ALTERNATE: Pour into 2 9-inch rounds, and bake for 30 minutes. When finished, apply a layer of frosting to one of the disks, then layer.  Top with frosting.</li>
</ol>
