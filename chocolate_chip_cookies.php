<ul id='ingredients'>
<li>2 sticks butter</li>
<li>1.5 cups brown sugar</li>
<li>1/2 cup white sugar</li>
<li>2 tbsp vanilla</li>
<li>2 eggs</li>
<li>1 egg yolk</li>
<li>.5 tsp salt</li>
<li>1.5 tsp baking soda</li>
<li>3.5 cups flour</li>
<li>2.5 cups chocolate chips</li>
</ul><br />
<ol id='process'>
<li>Cream butter and sugar together.</li>
<li>Add vanilla and mix in eggs.</li>
<li>Mix in dry ingredients thoroughly till smooth</li>
<li>Add chocolate chips.</li>
<li>Parcel out into spoonfuls on to greased cookie sheet. Bake at 350 F for 10 minutes.</li>
</ol>
