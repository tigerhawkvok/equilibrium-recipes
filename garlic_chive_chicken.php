<ul id='ingredients'>
  <!-- Ingredients in unordered list -->
  <li>1/2 head garlic</li>
  <li>8oz egg noodles</li>
  <li>1/2 cup chicken broth</li>
  <li>1/2 lemon, zested and juiced</li>
  <li>1/8 tsp salt</li>
  <li>1/2 tsp black pepper</li>
  <li>1 tsp olive oil</li>
  <li>2 chicken breasts</li>
  <li>1 Tbsp flour</li>
  <li>2 Tbsp butter</li>
  <li>2 Tbsp and 2 tsp (2.6 Tbsp = 8 tsp) chopped chives</li>
</ul>

<ol id='process'>
  <!-- Steps as ordered list elements -->
  <li>Preheat oven to 400 F. Wrap the garlic head in foil, and bake
  for 30 min or until cloves are soft. Let cool until you can handle
  it.</li>
  <li>Boil and cook egg noodles till done, and drain.</li>
  <li>Slice the top off of the garlic head, and squeeze the softened
  cloves into a medium bowl.</li>
  <li>Mix in chicken broth, lemon zest, lemon juice, salt, and
  pepper</li>
  <li>Heat the olive oil in a skillet over medium heat.</li>
  <li>Coat the chicken breast halves with flour (lightly), and cook in
  skillet ~10 min/side or until lightly browned.</li>
  <li>Set chicken aside, retaining juices in skillet.</li>
  <li>Stir in garlic mixture and bring to a boil</li>
  <li>Reduce heat, and return chicken to skillet. Continue cooking
  chicken ~5min/side until juices run clear.</li>
  <li>Remove chicken and serve over egg noodles</li>
  <li>Mix butter into garlic sauce mixture until melted, then stir in
  chives. Spoon sauce over chicken and egg noodles to serve.</li>
</ol>

