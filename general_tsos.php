<p class="title">General Tso's Chicken</p>
<ul>
<li>2 full chicken breasts</li>
<li>1 cup chopped green onions</li>
<li>1 cup broccoli heads</li><br />
<li>1/4 cup soy sauce</li>
<li>1 egg</li>
<li>2 cups flour (for coating)</li>
<li>vegetable oil (for frying)</li><br />
<li>3/8 cup sugar</li>
<li>1/4 cup corn starch</li>
<li>1/8 cup water</li>
<li>1/8 cup white wine</li>
<li>1/8 cup vinegar</li>
<li>1/4 cup soy sauce</li>
<li>1/2 can (~7 oz) chicken broth</li>
<li>garlic powder, ground ginger, chili flakes to taste</li>
</ul><br />
<ol>
<li>Mix egg and soy sauce. Cut chicken into bite sizes, coat in egg
  mixture, coat in flour, and drop into HOT oil.</li>
<li>Fry for 2-3 mins, then remove to paper towel. Continue until all
  chicken is cooked.</li>
<li>Mix sauce ingredients together, set aside.</li>
<li>Stir fry green onions and broccoli in HOT wok with a bit of oil
  for 45 seconds, add sauce mixture.</li>
<li>When sauce thickens, add chicken and mix until fully coated. Serve
  over rice</li>
</ol>
