<ul>
<li>2 sticks butter</li>
<li>1 cup sugar</li>
<li>1/4 cup brown sugar</li>
<li>1/2 cup molasses</li>
<li>1/8 cup maple syrup</li>
<li>1/8 cup honey</li>
<li>2 egg yolk</li>
<li>4 cups sifted all-purpose flour </li>
<li>1 teaspoon salt</li>
<li>1 teaspoon baking powder</li>
<li>1 teaspoon baking soda</li>
<li>1 teaspoon ground cinnamon</li>
<li>1 teaspoon ground nutmeg</li>
<li>2 teaspoon ground cloves</li>
<li>2 teaspoon ginger</li>
</ul>
<ol class='process'>
<li>In a large bowl, cream together the butter and sugar until
smooth.</li>
<li>Stir in molasses, honey, syrup, and egg yolk</li>
<li>Mix in flour, salt, baking powder, baking soda, cinnamon, cloves, ginger, and nutmeg; blend into
the molasses mixture until smooth..</li>
<li>
Preheat the oven to 350 degrees F (175 degrees C).  Put out
2cm radius dollops on greased cookie sheet</li>
<li>
Bake for 8 to 10 minutes in the preheated oven, until firm. Let cool 2-5 minutes before removing.</li>
</ol>




