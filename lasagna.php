<ul>
  <li>2 lbs ground beef (normal)</li>
  <li>1/2 cup minced onion</li>
  <li>5 cloves crushed or diced garlic</li>
  <li>28 oz crushed tomatoes</li>
  <li>12 oz tomato paste</li>
  <li>1/2 cup water</li>
  <li>2 tablespoons sugar</li>
  <li>1.5 tablespoons basil leaves</li>
  <li>1 tablespoon oregano</li>
  <li>1 teaspoon Italian seasoning</li>
  <li>1 tablespoon salt</li>
  <li>1/2 tablespoon black pepper</li>
  <li>4 tablespoons parsley</li>
  <li>12 lasagna noodles (at least; check fit in pan)</li>
  <li>15-16 oz ricotta cheese</li>
  <li>1 egg</li>
  <li>1/2 teaspoon salt</li>
  <li>1 lbs mozzarella cheese</li>
  <li>3/4 cup parmesan cheese</li>
</ul>

<ol class='process'>

  <li>In a covered saut&#233; pan, cook ground beef, onion, and garlic over medium heat until well browned. Stir in crushed tomatoes, tomato paste, and water. </li>
  <li>Season with sugar, basil, oregano, Italian seasoning, 1 tablespoon salt, pepper, and 2 of the 4 tablespoons parsley. </li>
  <li>Simmer, covered, for about 1 1/2 hours, stirring occasionally (every 10-15 minutes). </li>
  <li>In a mixing bowl, combine ricotta cheese with egg, remaining parsley, and 1/2 teaspoon salt. </li>
  <li>Bring a large pot of lightly salted water to a boil. Cook lasagna noodles in boiling water for 8 to 10 minutes. Drain noodles, and rinse with cold water. </li>
  <li>Preheat oven to 375 degrees F (190 degrees C). </li>
  <li>To assemble, spread 1 1/2 cups of meat sauce in the bottom of a 9x13 inch baking dish. Arrange 6 noodles lengthwise over meat sauce. Spread with one half of the ricotta cheese mixture. Top with a third of mozzarella cheese slices. Spoon 1 1/2 cups meat sauce over mozzarella, and sprinkle with 1/4 cup Parmesan cheese. Repeat layers, and top with remaining mozzarella and Parmesan cheese. Cover with foil: to prevent sticking, either spray foil with cooking spray, or make sure the foil does not touch the cheese. </li>
  <li>Bake in preheated oven for 25 minutes. Remove foil, and bake an additional 25 minutes. Cool for 5-10 minutes before serving.</li>

</ol>

