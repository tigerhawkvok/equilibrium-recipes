<ul id='ingredients'>
  <!-- Ingredients in unordered list -->
<li>1 pound ground beef</li>
  <li>2 eggs</li>
  <li>1/2 cup milk</li>
<li>2/3 cup oatmeal or bread crumbs</li>
<li>1/4-1/2 chopped onion</li>
<li>salt</li>
<li>pepper</li>
<li>1 T chopped sage</li></ul><br />
<ul>
<li>1/2 cup ketchup</li>
<li>1/4 cup brown sugar</li>
<li>1/2 t Worcestershire sauce</li>
</ul>

<ol id='process'>
  <!-- Steps as ordered list elements -->
<li>Preheat oven to 350.</li>
  <li>Beat eggs, add milk, crumbs/oatmeal, onions, sage, salt, pepper.</li>
  <li>Crumble beef into mixture and shape into 2 loaves.</li>
<li>Mix ketchup, brown sugar, and Worcestershire sauce.</li>
<li>Place loaves in baking dish lined with foil. Pour sauce over and
  cook 40-45 mins.</li>
</ol>

